import UIKit

extension UIColor {
    
    class func isYellowColor() -> UIColor {
        return UIColor(red: 253.0 / 255.0, green: 183.0 / 255.0, blue: 53.0 / 255.0, alpha: 1.0)
    }
    
    class func isGreenColor() -> UIColor {
        return UIColor(red: 120.0 / 255.0, green: 245.0 / 255.0, blue: 135.0 / 255.0, alpha: 1.0)
    }
    
    class func isRedColor() -> UIColor {
        return UIColor(red: 220.0 / 255.0, green: 28.0 / 255.0, blue: 135.0 / 255.0, alpha: 1.0)
    }
    
    class func isBrownColor() -> UIColor {
        return UIColor(red: 222.0 / 255.0, green: 188.0 / 255.0, blue: 153.0 / 255.0, alpha: 1.0)
    }
    
    class func isTumblrBlue() -> UIColor {
        return UIColor(red: 53.0 / 255.0, green: 70.0 / 255.0, blue: 91.0 / 255.0, alpha: 1.0)
    }
    
}